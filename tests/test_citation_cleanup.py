def test_clean_inline_source_markers_removes_azure_brackets():
    cleaned = clean_inline_source_markers(PLEADING_WITH_SOURCES)

    # No Azure inline citation blocks remain
    assert "【" not in cleaned
    assert "】" not in cleaned
    assert "†source" not in cleaned

    # Core Arabic content still present
    assert "الدفع الشكلي على الدعوى" in cleaned
    assert "الخلاصة" in cleaned
    assert "الطلبات" in cleaned


def test_clean_inline_handles_multiple_occurrences_and_whitespace():
    sample = "أ مثلاً ثم نص\n\n\nنهاية"
    cleaned = clean_inline_source_markers(sample)

    assert "【" not in cleaned and "】" not in cleaned
    # Collapses excessive blank lines to at most 2
    assert "\n\n\n" not in cleaned
    # Keeps the surrounding Arabic text intact
    assert cleaned.startswith("أ مثلاً")
    assert cleaned.endswith("نهاية") 